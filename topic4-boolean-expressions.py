# Boolean Expressions

# Test Values
x = 1
y = 2

# Note the order of operations with the following statements

# The values of both operands are equal
b1 = x == y
print(b1)

# The values of both operands are not equal.
b2 = x != y
print(b2)

# The first operand is larger in value than the second operand.
b3 = x > y
print(b3)

b4 = x < y
print(b4)

b5 = x >= y
print(b5)

b6 = x <= y
print(b6)

b7 = x is y
print(b7)

b8 = x is not y
print(b8)
